#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i] && n)
	{
		n--;
		i++;
	}
	if (s1[i] == s2[i] || n == 0)
		return (1);
	else
		return (0);
}
