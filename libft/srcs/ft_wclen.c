#include <libft.h>

size_t		ft_wclen(wchar_t w)
{
	if (w <= 1114111 && w > 65535)
		return (4);
	if (w <= 65535 && w > 2042)
		return (3);
	if (w <= 2042 && w > 127)
		return (2);
	if (w <= 127 && w >= 0)
		return (1);
	return ((size_t)-1);
}
